        //jTable1.convertRowIndexToModel(row);
        int value = Integer.parseInt(jTable1.getModel().getValueAt(row, 0).toString());
        TableModel tableModel = jTable1.getModel();
        nameField.setText(tableModel.getValueAt(row,1).toString());
        typeTextField.setText(tableModel.getValueAt(row,2).toString());
        priceTextField.setText(tableModel.getValueAt(row,3).toString());
        stockTextField.setText(tableModel.getValueAt(row,4).toString());
