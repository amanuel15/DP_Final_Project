    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        try{
            
            String sql = "UPDATE products SET name=?,type=?,price=?,stock=? WHERE name=?";
            pst = JDBCSingelton.getConnection().prepareStatement(sql);
            pst.setString(5, nameField.getText());
            pst.setString(1,nameField.getText());
            pst.setString(2,typeTextField.getText());
            pst.setString(3,priceTextField.getText());
            pst.setString(4,stockTextField.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Update successfull!");
            showTable();
            clearFields();

        } catch (SQLException | HeadlessException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_updateButtonActionPerformed

